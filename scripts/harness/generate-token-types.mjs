#!/usr/bin/env node
/**
 * G2 — semantic + component トークン名から TypeScript 型を生成
 */

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "../..");
const TOKENS_DIR = join(ROOT, "design-tokens");
const OUT_DIR = join(ROOT, "src/types");

function isTokenNode(obj) {
  return obj && typeof obj === "object" && "$value" in obj && !Array.isArray(obj);
}

function flattenPaths(obj, prefix = "") {
  /** @type {string[]} */
  const paths = [];
  if (!obj || typeof obj !== "object") return paths;
  for (const [key, value] of Object.entries(obj)) {
    if (key.startsWith("$")) continue;
    const path = prefix ? `${prefix}.${key}` : key;
    if (isTokenNode(value)) {
      paths.push(path);
    } else if (value && typeof value === "object") {
      paths.push(...flattenPaths(value, path));
    }
  }
  return paths;
}

function tokenPathToCssVar(path) {
  return `--${path.replace(/\./g, "-")}`;
}

function extractCssVars(cssContent) {
  const vars = new Set();
  const re = /var\(\s*(--[a-z0-9-]+)/gi;
  let m;
  while ((m = re.exec(cssContent)) !== null) {
    vars.add(m[1]);
  }
  return [...vars].sort();
}

const semantic = JSON.parse(
  readFileSync(join(TOKENS_DIR, "semantic.ai-dash.dark.json"), "utf8")
);
const components = JSON.parse(
  readFileSync(join(TOKENS_DIR, "components.json"), "utf8")
);
const primitives = JSON.parse(
  readFileSync(join(TOKENS_DIR, "primitives.json"), "utf8")
);

const semanticPaths = flattenPaths(semantic).sort();
const componentPaths = flattenPaths(components).sort();
const primitivePaths = flattenPaths(primitives).sort();
const allPaths = [...new Set([...semanticPaths, ...componentPaths])].sort();
const designPaths = [
  ...new Set([...semanticPaths, ...componentPaths, ...primitivePaths]),
].sort();

const pathByCssVar = new Map(designPaths.map((p) => [tokenPathToCssVar(p), p]));

const componentsCss = readFileSync(
  join(ROOT, "styles/components.css"),
  "utf8"
);
const cssVars = extractCssVars(componentsCss);

const unknownVars = cssVars.filter((v) => !pathByCssVar.has(v));
if (unknownVars.length > 0) {
  console.error("G2 generate FAIL — unknown CSS vars in components.css:");
  for (const v of unknownVars) console.error(`  ${v}`);
  console.error("  Fix: add token to semantic/components JSON or use existing var");
  process.exit(1);
}

mkdirSync(OUT_DIR, { recursive: true });

const dts = `// AUTO-GENERATED by scripts/harness/generate-token-types.mjs — do not edit
/** Semantic + component token paths (representative: semantic.ai-dash.dark.json) */
export type SemanticTokenPath =
${allPaths.map((p) => `  | '${p}'`).join("\n")};

/** Includes primitives for CSS var validation (G2 internal) */
export type DesignTokenPath =
${designPaths.map((p) => `  | '${p}'`).join("\n")};

/** CSS custom property name for a token path */
export type CssVarName<T extends SemanticTokenPath> = \`--\${T extends string ? T extends \`\${infer H}.\${infer R}\` ? \`\${H}-\${CssVarName<R & SemanticTokenPath>}\` : T : never}\`;

export type CssVarForToken<T extends SemanticTokenPath> = \`var(\${CssVarName<T>})\`;
`;

writeFileSync(join(OUT_DIR, "semantic-tokens.d.ts"), dts);

const registry = `// AUTO-GENERATED by scripts/harness/generate-token-types.mjs — do not edit
import type { DesignTokenPath } from "./semantic-tokens";

/** CSS vars used in styles/components.css — invalid names fail tsc (G2) */
export const CSS_TOKEN_REFS = [
${cssVars.map((v) => `  "${v}",`).join("\n")}
] as const;

/** Validated token paths referenced by components.css */
export const CSS_TOKEN_PATH_REFS = [
${cssVars.map((v) => `  "${pathByCssVar.get(v)}",`).join("\n")}
] as const satisfies readonly DesignTokenPath[];
`;

writeFileSync(join(OUT_DIR, "css-token-refs.ts"), registry);

const pathsExport = `// AUTO-GENERATED by scripts/harness/generate-token-types.mjs — do not edit
import type { SemanticTokenPath } from "./semantic-tokens";

export const SEMANTIC_TOKEN_PATHS = [
${allPaths.map((p) => `  "${p}",`).join("\n")}
] as const satisfies readonly SemanticTokenPath[];
`;

writeFileSync(join(OUT_DIR, "semantic-token-paths.ts"), pathsExport);

console.log(`Generated ${allPaths.length} token paths → src/types/`);
